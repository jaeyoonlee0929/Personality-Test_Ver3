# --- 공통 함수 ---
def call_gpt(text, prompt_template):
    if pd.isna(text) or str(text).strip() == "": return "No valid input."
    prompt = prompt_template.format(text=text)
    try:
        response = client.chat.completions.create(model="gpt-4.1", messages=[{"role": "user", "content": prompt}], temperature=0.5)
        return response.choices[0].message.content.strip()
    except Exception as e:
        st.warning(f"GPT API 호출 중 오류가 발생했습니다: {e}")
        return f"API Error: {e}"

