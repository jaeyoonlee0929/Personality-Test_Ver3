def process_personality(worksheet):
    data = worksheet.get_all_values()
    headers, df = data[0], pd.DataFrame(data[1:], columns=data[0])
    new_headers = ["Point Form (B)", "Summary (C)", "Key Insights (D)", "Final", "Ultimate"]
    for i, h in enumerate(new_headers): worksheet.update_cell(1, len(headers) + 1 + i, h)
    
    progress_bar = st.progress(0)
    for index, row in df.iterrows():
        status_text = st.empty()
        row_num = index + 2
        original_text = row[headers[1]]

        if not original_text or pd.isna(original_text): continue
        
        status_text.info(f"행 {row_num}: (1/5) 요인 추출 중...")
        point_form = call_gpt(original_text, prompt_personality_1)
        worksheet.update_cell(row_num, len(headers) + 1, point_form)
        
        status_text.info(f"행 {row_num}: (2/5) 요인 조합 생성 중...")
        summary = call_gpt(point_form, prompt_personality_2)
        worksheet.update_cell(row_num, len(headers) + 2, summary)

        status_text.info(f"행 {row_num}: (3/5) 조합별 4문장 생성 중...")
        insights = call_gpt(summary, prompt_personality_3)
        worksheet.update_cell(row_num, len(headers) + 3, insights)

        status_text.info(f"행 {row_num}: (4/5) 문체 변환 중...")
        final = call_gpt(insights, prompt_personality_4)
        worksheet.update_cell(row_num, len(headers) + 4, final)

        status_text.info(f"행 {row_num}: (5/5) 최종 요약 문장 생성 중...")
        ultimate = call_gpt(final, prompt_personality_5)
        worksheet.update_cell(row_num, len(headers) + 5, ultimate)
        
        progress_bar.progress((index + 1) / len(df))
        status_text.success(f"행 {row_num} 분석 완료!")
        time.sleep(0.5)

